#include <stdio.h>

/*
        Aula 24
    operadores matemáticos para:
    Soma              +
    Subtração         -
    Multiplicação     *
    Divisão           /
*/

int main(void) {
    printf("\n\nSoma: %d\n\n",10 + 20);
    printf("\n\nSubtracao %d",10 - 3);
    printf("\n\nMultiplicacao %d",10 * 2);
    printf("\n\nDivisao %f",10.0 / 2);
    return 0;
}
